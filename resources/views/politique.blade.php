<h6 class= "blanc containerpol">  Dans le cadre de son activité les membres du BDE CESI Strasbourg sont amenés à traiter des informations vous concernant.
 <br>
 
 Cette Politique de confidentialité vous informe de la manière dont nous recueillons et traitons vos données personnelles.<br> Ainsi nous vous invitons à la lire attentivement.
<br>
<br>
<b>Sachez que nous recueillons certaines informations lorsque :</b>
<br>
<br>
•	Vous vous créez un compte.<br>
•	Vous partagez des publications.<br> 
•	Vous nous adressez des courriels. <br>
•	Vous achetez des articles dans notre boutique.<br>
<br>
<b>Ces données sont notamment :</b>
<br><br>
•	Noms, prénoms, ville du campus ;<br>
•	Adresse postale, adresse e-mail, numéro de téléphone ;<br>
•	Informations nécessaires à la livraison ou au portage d’un produit commandé ;<br>
•	Identifiant et mot de passe crypté utilisés pour vous identifier sur nos sites ou nos applications ;<br>
•	Informations cryptées relatives à vos moyens de paiement (notamment numéro de carte bancaire) <br>
<br>
Mais également les données recueillies automatiquement lors de chacune de vos visites sur notre site.<br> 
En effet nous recueillons des informations relatives à votre connexion et à votre navigation. <br>Différentes technologies peuvent être mises en œuvre pour recueillir ces données. La principale est le cookie.
<br>
<br>Un « cookie » est un fichier texte déposé sur votre ordinateur lors de la visite de notre plateforme. <br>Dans votre ordinateur, les cookies sont gérés par votre navigateur internet.<br>
Nous utilisons des cookies sur notre site pour les besoins de votre navigation, l’optimisation <br>et la personnalisation de nos services sur notre plateforme en mémorisant vos préférences.<br>
Les cookies nous permettent aussi de voir comment notre plateforme est utilisée.<br>
Nous recueillons automatiquement votre adresse IP et des informations relatives à l’utilisation de notre Site.<br> Notre plateforme peut ainsi se souvenir de votre identité lorsqu’une connexion a été établie entre le serveur et le navigateur web. <br>Les informations fournies précédemment dans un formulaire web pourront ainsi être conservées.
<br>
<br>
<b>Nous nous engageons à :</b><br>
<br>
Décrire dans cette Politique de confidentialité, comment vos informations sont susceptibles d’être utilisées ou communiquées.<br>
Mettre en œuvre des mesures qui permettront d’assurer la sécurité de vos informations personnelles.<br>
Ne jamais vendre  ou communique vos informations personnelles à des tierces.<br>
<br>
<b>Soyez conscient que:</b><br>
<br>
•	Tout contenu que vous ajoutez ou toute modification que vous apportez sur ce site sera accessible à tous de manière permanente.<br> 
•	Les membres du BDE utilisent des outils qui leur donnent un accès limité à des informations non publiées vous concernant qu’ils utilisent pour protéger le site et faire respecter les réglementations.<br>
•	Pour la protection de notre site et des autres utilisateurs, si vous n’êtes pas d’accord avec la présente Politique de confidentialité, vous n’êtes pas autorisé à utiliser le site du BDE de Strasbourg.<br>
<br>
<b>Vos droits :</b><br>
<br>
Vous avez le droit d’accéder à vos données personnelles et de demander qu’elles soient rectifiées, complétées ou mises à jour. <br>Vous pouvez également demander l’effacement de vos données ou vous opposer à leur traitement, à condition de justifier d’un motif légitime.
<br>Vous pouvez enfin formuler des directives relatives à la conservation, à l’effacement et à la communication de vos données à caractère personnel après votre décès.<br>

<br>Vous pouvez exercer vos droits par mail : bde.strasbourg@viacesi.fr.<br>

<br>Avant de répondre à votre demande, nous sommes susceptibles de vérifier votre identité et/ou vous demander de nous fournir davantage d’informations pour répondre à votre demande.<br> Nous nous efforcerons de donner suite à votre demande dans un délai raisonnable et, en tout état de cause, dans les délais fixés par la loi.
<br>En cas de réponse insatisfaisante, vous pouvez introduire une réclamation auprès de la Commission nationale de l’informatique et des libertés (CNIL) 
